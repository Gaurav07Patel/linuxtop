
def add(n1,n2,n3,n4):
    addition =  n1+n2+n3+n4
    print(addition)

def sub(n1,n2,n3,n4):
    subtraction = n1-n2-n3-n4 
    print(subtraction)

def mult(n1,n2,n3,n4):
    multiplication = n1*n2*n3*n4 
    print(multiplication)
         
def div(n1,n2,n3,n4):
    division = n1/n2/n3/n4  
    print(division)

print(add(8,4,2,1))
print(sub(8,4,2,1))
print(mult(8,4,2,1))
print(div(8,4,2,1))
